public class Rectangle
{
    // Atributos
    private double width;
    private double height;

    // Constructores

    // Getters

    // Setters
    public void setWidth(double width)
    {
        this.width = width;
    }

    public void setHeight(double height)
    {
        this.height = height;
    }

    // Métodos públicos
    public double calculateArea()
    {
        return this.width * this.height;
    } 

    // Métodos privados
}
